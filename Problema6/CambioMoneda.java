package Problemas_Practica.Problema6;

public class CambioMoneda {
    //------------METODOS--------
    private double dolar, pesosColombianos;

    //----------Constructor---------------------
    public CambioMoneda(double dolar) {
        this.dolar = 0.0023;
        this.pesosColombianos= pesosColombianos;
    }
    public double cacularCambioMenda(){
        return dolar*pesosColombianos;
    }
}
